#!/usr/bin/env node
// ═══════════════════════════════════════════════════════════════════════
// build-part-icons.mjs — auto-generate a Metro-bundled require() registry
// for every Sidekick part thumbnail in src/assets/images/parts/.
//
// The Unity Drop4Exporter writes one PNG per part (e.g.
//   SK_MDRN_CIVL_01_10TORS_HU01.png
// ). This script scans that folder and emits a TS module that maps each
// part name → require() source, so AmgPartCard can render it via:
//
//   import { getPartThumb } from '../../data/partThumbs';
//   const src = getPartThumb(partName);
//
// Re-run after every Unity batch render. Idempotent — overwrites the
// existing partThumbs.ts each time.
// ═══════════════════════════════════════════════════════════════════════

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const PARTS_DIR = path.join(ROOT, 'src', 'assets', 'images', 'parts');
const OUT_FILE = path.join(ROOT, 'src', 'data', 'partThumbs.ts');

if (!fs.existsSync(PARTS_DIR)) {
  console.error(`Parts folder not found: ${PARTS_DIR}`);
  console.error(`Run the Unity "Tools/Export Part Thumbnails to Drop4" first.`);
  process.exit(1);
}

const files = fs.readdirSync(PARTS_DIR)
  .filter((f) => f.endsWith('.png'))
  .sort();

if (files.length === 0) {
  console.error(`No PNGs in ${PARTS_DIR}.`);
  process.exit(1);
}

// Each line in the map is:
//   '<PART_NAME>': require('../assets/images/parts/<file>.png'),
// Metro requires the path to be a string literal at build time, so we
// emit one entry per part instead of computing paths dynamically.
const lines = files.map((f) => {
  const partName = path.basename(f, '.png');
  return `  '${partName}': require('../assets/images/parts/${f}'),`;
});

const banner = `// AUTO-GENERATED by tools/build-part-icons.mjs — DO NOT EDIT BY HAND.
// Re-run \`node tools/build-part-icons.mjs\` after every Unity part batch.
//
// Maps Sidekick part names → bundled PNG thumbnail. Each thumb is a
// 256×256 transparent PNG rendered by Drop4Exporter in the AMG Studios
// Unity Synty Character Creator project. AmgPartCard reads this via
// getPartThumb(partName) and falls back to the pack-cover icon if the
// part isn't in the map (e.g. a fresh pack added before re-running the
// Unity render).
//
// Source folder: src/assets/images/parts/
// Source script: ../../Unity Games/Synty Character Creator/Assets/CharacterCreator/Scripts/Drop4Exporter.cs
// Trigger menu:  Tools → Export Part Thumbnails to Drop4`;

const file = `${banner}

import type { ImageSourcePropType } from 'react-native';

export const PART_THUMB: Record<string, ImageSourcePropType> = {
${lines.join('\n')}
};

export function getPartThumb(partName: string): ImageSourcePropType | undefined {
  return PART_THUMB[partName];
}

export const PART_THUMB_COUNT = ${files.length};
`;

fs.writeFileSync(OUT_FILE, file);
console.log(`Wrote ${OUT_FILE}`);
console.log(`  ${files.length} part thumbnails registered.`);
